// 😄 3.Control Flow (เงื่อนไขและลูป)
//
// เงื่อนไข (if-else)
//==============================================================================================

void main() {
  int score = 85;

  if (score >= 80) {
    print('Grade: A');
  } else if (score >= 70) {
    print('Grade: B');
  } else {
    print('Grade: C');
  }
}
